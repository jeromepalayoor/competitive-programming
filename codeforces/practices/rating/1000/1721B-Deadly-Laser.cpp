int solve() {
	int n,m,sx,sy,d;
	cin>>n>>m>>sx>>sy>>d;
	if (min(sx - 1, m - sy) <= d && min(n - sx, sy - 1) <= d)
	{
		cout<<-1<<endl;
		return 0;
	}
	cout<<n+m-2<<endl;
	return 0;
}

int32_t main() {
	ios::sync_with_stdio(0);
	cin.tie(0);
	int TET = 1e9;
	cin >> TET;
	for (int i = 1; i <= TET; i++) {
		if (solve()) {
			break;
		}
	}
}