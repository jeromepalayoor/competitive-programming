typedef long long ll;
typedef long double ld;

int solve() {
	int a,b,n;
	cin>>a>>b>>n;
	int x[n];
	for (int i = 0; i < n; ++i)
	{
		cin>>x[i];
	}
	ll t=b;
	for (int i = 0; i < n; ++i)
	{
		t+=min(a-1,x[i]);
	}
	cout<<t<<endl;
	return 0;
}

int32_t main() {
	ios::sync_with_stdio(0);
	cin.tie(0);
	int TET = 1e9;
	cin >> TET;
	for (int i = 1; i <= TET; i++) {
		if (solve()) {
			break;
		}
	}
}